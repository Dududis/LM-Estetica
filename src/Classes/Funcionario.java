package Classes;

public class Funcionario {

	private String nome;
	private String sobrenome;
	private int rg;
	private int cpf;
	private int credencial;
	private int salario;
	private int contabancaria;
	
	public String getNome(String nome) {
		   return nome;
	}	
		
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getSobrenome(String sobrenome) {
		   return sobrenome;
	}	
	
	public void setSobrenome(String sobrenome) {
		this.sobrenome = sobrenome;
	}
	
	public int getRg(int rg) {
		   return rg;
	}	
		
	public void setRg(int rg) {
		this.rg = rg;
	}
	
	public int getCpf(int cpf) {
		   return cpf;
	}	
		
	public void setCpf(int cpf) {
		this.cpf = cpf;
	}
	
	public int getCredencial(int credencial) {
		   return credencial;
	}	
		
	public void setCredencial(int credencial) {
		this.credencial = credencial;
	}
	
	public int getSalario(int salario) {
		   return salario;
	}	
		
	public void setSalario(int salario) {
		this.salario = salario;
	}
	
	public int getContabancaria(int contabancaria) {
		   return contabancaria;
	}	
		
	public void setContabancaria(int contabancaria) {
		this.contabancaria = contabancaria;
	}
	
	
	
}// fim
